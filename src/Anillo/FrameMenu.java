    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int idmayor=0;
        this.jTextArea1.setText("");
        String total="";
        String res="";
        if(this.jList1.getSelectedIndex()==-1){
            for(Proceso p : this.procesos){
                if(!p.isFallando()){
                    res= p.crearLista(res);
                    total+=res+"\n";
                    this.jTextArea1.append(res+"\n");
                    if(idmayor<p.getId())
                        idmayor=p.getId();
                }
            }
        }else{
            for(int i=this.jList1.getSelectedIndex(); i<this.procesos.size();i++){
                if(!this.procesos.get(i).isFallando()){
                    res= this.procesos.get(i).crearLista(res);
                    total+=res+"\n";
                    this.jTextArea1.append(res+"\n");
                    if(idmayor<this.procesos.get(i).getId())
                        idmayor=this.procesos.get(i).getId();
                }
            }
            for(int i=0; i<this.jList1.getSelectedIndex();i++){
                if(!this.procesos.get(i).isFallando()){
                    res= this.procesos.get(i).crearLista(res);
                    total+=res+"\n";
                    this.jTextArea1.append(res+"\n");
                    if(idmayor<this.procesos.get(i).getId())
                        idmayor=this.procesos.get(i).getId();
                }
            }
        }
        this.coordi.setText(idmayor+"");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int indice= this.jList1.getSelectedIndex();
        if(indice!=-1){
            for (int i = 0; i < this.procesos.size(); i++) {
                if(indice+1== this.procesos.get(i).getId()){
                    this.procesos.get(i).fallar();
                    if(this.procesos.get(i).getId()== Integer.parseInt(this.coordi.getText()))
                        this.coordi.setText("--");
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int indice= this.jList1.getSelectedIndex();
        if(indice!=-1){
            for (int i = 0; i < this.procesos.size(); i++) {
                if(indice+1== this.procesos.get(i).getId()){
                    this.procesos.get(i).reanudar();
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

