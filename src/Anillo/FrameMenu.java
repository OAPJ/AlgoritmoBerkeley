    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Proceso p = new Proceso(numPs++);
        this.procesos.add(p);
        p.setAnterior(this.procesos.get(this.procesos.size()-1));
        p.setSiguiente(this.procesos.get(0));
        if(this.procesos.size()!=1){
            this.procesos.get(this.procesos.size()-2).setSiguiente(p);
            this.procesos.get(0).setAnterior(p);
        }
        this.modelo.addElement("P:"+p.getId()+"");
        this.jList1.setModel(modelo);
        
    }//GEN-LAST:event_jButton1ActionPerformed

